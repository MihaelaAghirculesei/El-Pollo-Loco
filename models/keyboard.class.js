class Keyboard {
  /** @type {boolean} Indicates if the left key is pressed */
  LEFT = false;
  
  /** @type {boolean} Indicates if the right key is pressed */
  RIGHT = false;
  
  /** @type {boolean} Indicates if the up key is pressed */
  UP = false;
  
  /** @type {boolean} Indicates if the down key is pressed */
  DOWN = false;
  
  /** @type {boolean} Indicates if the space key is pressed */
  SPACE = false;
  
  /** @type {boolean} Indicates if the 'D' key is pressed */
  D = false;
}
